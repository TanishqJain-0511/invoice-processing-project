export default function Sidebar() {
  const path = usePathname();
  const [companyName, setCompanyName] = useState<string>("");

  function loadCompanyName() {
    try {
      const info = localStorage.getItem("invoiceProcessor:companyInfo");
      if (info) {
        const parsed = JSON.parse(info);
        setCompanyName(parsed.name || "");
      } else {
        setCompanyName("");
      }
    } catch {}
  }

  useEffect(() => {
    loadCompanyName();
    window.addEventListener("storage", loadCompanyName);
    return () => window.removeEventListener("storage", loadCompanyName);
  }, []);

  return (
    <aside className="w-52 shrink-0 h-screen sticky top-0 bg-white border-r border-slate-200 flex flex-col">
      {/* Brand */}
      <div className="px-4 py-5 border-b border-slate-100">
        <p className="text-sm font-bold text-slate-900 truncate">
          {companyName || "Invoice Processor"}
        </p>
        {companyName && (
          <p className="text-xs text-slate-400 mt-0.5 truncate">AI-powered AP automation</p>
        )}
      </div>

      {/* Nav */}
      <nav className="flex-1 p-3 space-y-0.5">
        {NAV.map((item) => {
          const active = item.exact ? path === item.href : path.startsWith(item.href);
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-2.5 px-3 py-2 rounded-lg text-sm transition-colors ${
                active
                  ? "bg-slate-100 text-slate-900 font-semibold"
                  : "text-slate-500 hover:bg-slate-50 hover:text-slate-800"
              }`}
            >
              <span className={active ? "text-slate-700" : "text-slate-400"}>
                {item.icon}
              </span>
              {item.label}
            </Link>
          );
        })}
      </nav>

      {/* Footer */}
      <div className="px-4 py-3 border-t border-slate-100">
        <p className="text-xs text-slate-400">PS-1 · Invoice Pipeline</p>
      </div>
    </aside>
  );
}
